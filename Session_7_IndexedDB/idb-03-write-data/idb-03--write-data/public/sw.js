//2. 1 is the version
//3. passed function

var dbPromise = idb.open('posts-store', 1, function (db) {
  //If there is no created posts database
  if (!db.objectStoreNames.contains('posts')) {
    //create a posts database, where the keyPath will be the unique value based on what the data can be accessed
    db.createObjectStore('posts', {keyPath: 'id'});
  }
});

