self.addEventListener('install', function (event) {
  console.log('[Service Worker] Installing Service Worker ...', event);
  event.waitUntil(
    caches.open(CACHE_STATIC_NAME)
      .then(function (cache) {
        console.log('[Service Worker] Precaching App Shell');
        cache.addAll(STATIC_FILES);
      })
  )
});

self.addEventListener('activate', function (event) {
  console.log('[Service Worker] Activating Service Worker ....', event);
  event.waitUntil(
    caches.keys()
      .then(function (keyList) {
        return Promise.all(keyList.map(function (key) {
          if (key !== CACHE_STATIC_NAME && key !== CACHE_DYNAMIC_NAME) {
            console.log('[Service Worker] Removing old cache.', key);
            return caches.delete(key);
          }
        }));
      })
  );
  return self.clients.claim();
});

function isInArray(string, array) {
  var cachePath;
  if (string.indexOf(self.origin) === 0) { // request targets domain where we serve the page from (i.e. NOT a CDN)
    console.log('matched ', string);
    cachePath = string.substring(self.origin.length); // take the part of the URL AFTER the domain (e.g. after localhost:8080)
  } else {
    cachePath = string; // store the full request (for CDNs)
  }
  return array.indexOf(cachePath) > -1;
}

//1. We are intercepting url where we are trying to fetch some data from firebase
//2. We fetch it from the server
//3. Clone the response
//4. Loop over the recieved json data
//5. Initiating the IDB Object
//6. Which store we want to target and which kind of transactions do we want to execute
//7. We have to open the store
//8. We can put the data, which will have an id property, which will be the keyPath, identifier
//9. Will return the transaction with tx.complete, which will exexcute all idb related transaction 
self.addEventListener('fetch', function (event) {

  var url = 'https://pwagram-99adf.firebaseio.com/posts';       //1.
  if (event.request.url.indexOf(url) > -1) {
    event.respondWith(fetch(event.request)                      //2. 
      .then(function (res) {
        var clonedRes = res.clone();
        clonedRes.json()                                        //3.
          .then(function(data) {
            for (var key in data) {                             //4.
              dbPromise                                         //5.
                .then(function(db) {                 
                  var tx = db.transaction('posts', 'readwrite'); //6.
                  var store = tx.objectStore('posts');           //7.
                  store.put(data[key]);                          //8.
                  return tx.complete;                            //9.
                });
            }
          });
        return res;
      })
    );
  } else if (isInArray(event.request.url, STATIC_FILES)) {
    event.respondWith(
      caches.match(event.request)
    );
  } else {
    event.respondWith(
      caches.match(event.request)
        .then(function (response) {
          if (response) {
            return response;
          } else {
            return fetch(event.request)
              .then(function (res) {
                return caches.open(CACHE_DYNAMIC_NAME)
                  .then(function (cache) {
                    // trimCache(CACHE_DYNAMIC_NAME, 3);
                    cache.put(event.request.url, res.clone());
                    return res;
                  })
              })
              .catch(function (err) {
                return caches.open(CACHE_STATIC_NAME)
                  .then(function (cache) {
                    if (event.request.headers.get('accept').includes('text/html')) {
                      return cache.match('/offline.html');
                    }
                  });
              });
          }
        })
    );
  }
});

